#include <stdio.h>
/**
 * main - My principal function
 * int - My variable 'i' set to '48'
 * while - loop runs while 'i' is less than or equal to '57'
 * putchar('\n') - print a newline
 * Return: 0*
*/
int main(void)
{
	int i = 48;

	while (i <= 57)
	{
		putchar(i);
		i++;
		
	}
	putchar('\n');
	return (0);
}

